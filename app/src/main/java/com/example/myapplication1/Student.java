package com.example.myapplication1;

public class Student {
    String name;
    String surname;
    int age;
    String classroom;
    int id;

    public Student(String name, String surname, int age, String classroom, int id) {
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.classroom = classroom;
        this.id = id;
    }
}
